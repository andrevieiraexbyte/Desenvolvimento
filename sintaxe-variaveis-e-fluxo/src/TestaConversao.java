
public class TestaConversao {
	
	public static void main(String[] args) {
		
		double salario = 1270.70;
		
		
		
		int valor = (int) salario; // cast fazendo converssão de double para int
		System.out.println(valor);
		
		float outroValor = 15.54f; // declarando de forma literal que é float utilizando o f no final do número
		System.out.println(outroValor);
	
	}
}
